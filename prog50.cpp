#include<bits/stdc++.h>
using namespace std;
template<typename T>
T sum(T n, T m)
{
    T result;
    result = n + m;
    return result;
}
int main()
{
    int a, b,c;
    cin>>a>>b;
    c = sum(a,b);
    cout<<c<<endl;
    float l,k,o;
    cin>>l>>k;
    o = sum(l,k);
    cout<<o<<endl;
}
